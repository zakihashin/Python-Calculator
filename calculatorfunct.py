def add(num1,num2):
    sum1 = num1 + num2
    return sum1
def sub(num1,num2):
    sum2 = num1 - num2
    return sum2
def mul(num1,num2):
    sum3 = num1 * num2
    return sum3
def div(num1,num2):
    sum4 = num1 / num2
    return sum4
addition = add(1,54)
subtraction = sub(546,12)
multiplication = mul(23,43)
division = div(44,11)
print(addition)
